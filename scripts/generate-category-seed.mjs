// Generates supabase/categories-seed.sql from the category JSON the client serves
// (public/categories/index.json + each listed file). The seed mirrors those
// categories + items into the DB so daily averages can be derived WITH item
// identity (see public.snapshot_daily_averages in supabase/schema.sql). The client
// keeps loading the JSON; this only keeps the DB copy in sync.
//
// Reading the same manifest + files the client reads guarantees the DB matches
// exactly what players see (including any hand-edited day). Re-run after the
// categories change, then apply the seed in the Supabase SQL editor:
//   node scripts/generate-category-seed.mjs
//
// Source-of-truth chain: scripts/calendar.txt -> public/categories/*.json (via
// generate-categories.mjs, served to the client) -> supabase/categories-seed.sql
// (via this script, applied to the DB).

import { readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const DIR = join(ROOT, "public", "categories");
const OUT = join(ROOT, "supabase", "categories-seed.sql");

// Single-quoted SQL string literal with quotes escaped. Emoji/UTF-8 pass through.
const q = (s) => `'${String(s ?? "").replace(/'/g, "''")}'`;

async function main() {
  const manifest = JSON.parse(await readFile(join(DIR, "index.json"), "utf8"));
  const files = Array.isArray(manifest) ? manifest : manifest.files || [];
  const configs = await Promise.all(
    files.map((f) => readFile(join(DIR, f), "utf8").then((t) => JSON.parse(t)))
  );

  const catRows = configs.map(
    (c) => `  (${q(c.date)}, ${q(c.name)}, ${q(c.theme)}, ${q(c.special_day)})`
  );

  const itemRows = [];
  for (const c of configs) {
    c.items.forEach((it, i) => {
      itemRows.push(`  (${q(c.date)}, ${i}, ${q(it.id)}, ${q(it.name)}, ${q(it.emoji)})`);
    });
  }

  const days = configs.map((c) => q(c.date)).join(", ");

  const sql = [
    "-- AUTO-GENERATED by scripts/generate-category-seed.mjs — do not edit by hand.",
    "-- Mirrors public/categories/*.json (what the client serves) into the DB so",
    "-- daily averages can be derived with item identity. Run AFTER schema.sql and",
    "-- BEFORE daily-averages-setup.sql. Idempotent — safe to re-run.",
    "",
    "begin;",
    "",
    "insert into public.categories (day, name, theme, special_day) values",
    catRows.join(",\n"),
    "on conflict (day) do update",
    "  set name = excluded.name, theme = excluded.theme, special_day = excluded.special_day;",
    "",
    "-- Replace each listed day's items wholesale so edits/reorders are reflected,",
    "-- while leaving any day no longer in the calendar untouched (history stays).",
    `delete from public.category_items where day in (${days});`,
    "insert into public.category_items (day, position, item_id, name, emoji) values",
    itemRows.join(",\n") + ";",
    "",
    "commit;",
    ""
  ].join("\n");

  await writeFile(OUT, sql);
  console.log(`Wrote ${OUT}: ${configs.length} categories, ${itemRows.length} items.`);
}

main().catch((err) => {
  console.error(err.message);
  process.exit(1);
});
